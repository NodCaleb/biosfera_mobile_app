#pragma mark - Navigation

// In a storyboard-based application, you will often want to do a little preparation before navigation
- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    if ([segue.identifier isEqualToString:@"showProducts"])
        if ([segue.destinationViewController isKindOfClass:[ProductsVC class]])
        {
            NSIndexPath *path = [self.tableView indexPathForSelectedRow];
            ProductsVC *targetVC = segue.destinationViewController;
            //targetVC.upperCategoryNumber = path.row;
            targetVC.navigationBarTitle = [self.categories objectAtIndex:path.row][CATEGORY_NAME];
        }
}


