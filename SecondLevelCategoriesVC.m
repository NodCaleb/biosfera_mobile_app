- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"secondLevelCategoryCell" forIndexPath:indexPath];
    
    NSDictionary *categoryData = [self.categories objectAtIndex:indexPath.row];
    cell.textLabel.text = categoryData[CATEGORY_NAME];
    
    return cell;
}


